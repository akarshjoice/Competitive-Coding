/* Function to insert into a Doubly Linked List */
void addNode(Node *head, int pos, int data)
{
   Node *tmp = new Node(data);
   int i=0;
   Node *curr = head;
   while(curr != NULL && i<pos) {
       curr=curr->next;
       i++;
   }
   if(curr == NULL) {
       return;
   }
   Node *tmp1 = curr->next;
   curr->next = tmp;
   tmp->prev = curr;
   tmp->next = tmp1;
   if(tmp1 != NULL)
    tmp1->prev = tmp;
}