// Function for finding maximum and value pair
int maxAND (int arr[], int n)
{  
    int res = 0; 
    int count;
    for(int j=31;j>=0;j--) {
        unsigned int p =res | (1<<j);
        count = 0;
        for(int i=0;i<n;i++) {
            
            if((arr[i]&p) == p) {
                count++;
            }
        }
        if(count >= 2) {
            res = res | (1<<j); 
        }
    }
    return res;
    
}

